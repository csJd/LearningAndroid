public class Call {
    int id;
    String tel, time;

    public int getId() {
        return id;
    }

    public String getTel() {
        return tel;
    }

    public String getTime() {
        return time;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
